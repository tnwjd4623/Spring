package com.example.demo;

public class Movie {
	private Integer m_no;
	private String m_title;
	private String m_img;
	private String m_pubDate;
	private String m_director;
	private String m_rating;
	
	public Integer getM_no() {
		return m_no;
	}
	public void setM_no(Integer m_no) {
		this.m_no = m_no;
	}
	public String getM_title() {
		return m_title;
	}
	public void setM_title(String m_title) {
		this.m_title = m_title;
	}
	public String getM_img() {
		return m_img;
	}
	public void setM_img(String m_img) {
		this.m_img = m_img;
	}
	public String getM_pubDate() {
		return m_pubDate;
	}
	public void setM_pubDate(String m_pubDate) {
		this.m_pubDate = m_pubDate;
	}
	public String getM_director() {
		return m_director;
	}
	public void setM_director(String m_director) {
		this.m_director = m_director;
	}
	public String getM_rating() {
		return m_rating;
	}
	public void setM_rating(String string) {
		this.m_rating = string;
	}
	

}
